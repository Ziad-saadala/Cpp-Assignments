
// // Output Needed
// "ASCII Value of ~ Is 126"
// "ASCII Value of & Is 38"
// "ASCII Value of % Is 37"
// "ASCII Value of A Is 65"

#include <iostream>
using namespace std;

int main ()
{

    char a = '~';
    char b = '&';
    char c = '%';
    char d = 'A';


    cout << "The ASCII value for " << a << " = " << int(a) << endl; 
    cout << "The ASCII value for " << b << " = " << int(b) << endl; 
    cout << "The ASCII value for " << c << " = " << int(c) << endl; 
    cout << "The ASCII value for " << d << " = " << int(d) << endl; 


    return 0;
}

